package net.mynym.doxy.main;

/*
 * The creation phase is informal and collaborative, allowing one or more authors to create
 * and update drafts freely. Consultation is a wider, more formal process in which those consulted
 * comment on and suggest changes to a draft. Those consulted may change the document or simply offer
 * comments for the authors to act on. Validation or endorsement is the process of gaining approval
 * from authorities for the document. Endorsement typically does not change the document.
 */
public enum Stages {
	CREATION, CONSULTATION, ENDORSEMENT
} 
